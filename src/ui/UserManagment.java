    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String username = usernameTxt.getText().trim();
        String password = passwordTxt.getText().trim();
        String role = (String) roleCombobox.getSelectedItem();

        // Validation
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password");
            return;
        }

        try {
            con = db.getConnection();

            // Check if username already exists
            String checkQuery = "SELECT COUNT(*) FROM user WHERE user_name = ?";
            pst = con.prepareStatement(checkQuery);
            pst.setString(1, username);
            rs = pst.executeQuery();
            rs.next();
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Username already exists!");
                con.close();
                return;
            }
            rs.close();
            pst.close();

            // Insert new user
            String insertQuery = "INSERT INTO user (user_name, access_code, role) VALUES (?, ?, ?)";
            pst = con.prepareStatement(insertQuery);
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, role);

            int result = pst.executeUpdate();
            con.close();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "User added successfully!");
                loadUserTable();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add user!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding user: " + e.getMessage());
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (selectedUserId == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user from table to update");
            return;
        }

        String username = usernameTxt.getText().trim();
        String password = passwordTxt.getText().trim();
        String role = (String) roleCombobox.getSelectedItem();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password");
            return;
        }

        try {
            con = db.getConnection();

            // Check if new username already exists
            String checkQuery = "SELECT COUNT(*) FROM user WHERE user_name = ? AND user_id != ?";
            pst = con.prepareStatement(checkQuery);
            pst.setString(1, username);
            pst.setInt(2, selectedUserId);
            rs = pst.executeQuery();
            rs.next();
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Username already exists!");
                con.close();
                return;
            }
            rs.close();
            pst.close();

            // Update user
            String updateQuery = "UPDATE user SET user_name = ?, access_code = ?, role = ? WHERE user_id = ?";
            pst = con.prepareStatement(updateQuery);
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, role);
            pst.setInt(4, selectedUserId);

            int result = pst.executeUpdate();
            con.close();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "User updated successfully!");
                loadUserTable();
                clearFields();
                selectedUserId = -1;
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update user!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating user: " + e.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        if (selectedUserId == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user from table to delete");
            return;
        }

        // Prevent deleting own account
        String currentUser = "admin";
        String selectedUsername = usernameTxt.getText();
        if (selectedUsername.equals(currentUser)) {
            JOptionPane.showMessageDialog(this, "You cannot delete your own account!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete user: " + selectedUsername + "?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                con = db.getConnection();
                String deleteQuery = "DELETE FROM user WHERE user_id = ?";
                pst = con.prepareStatement(deleteQuery);
                pst.setInt(1, selectedUserId);

                int result = pst.executeUpdate();
                con.close();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "User deleted successfully!");
                    loadUserTable();
                    clearFields();
                    selectedUserId = -1;
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete user!");
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error deleting user: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

