    private void loadUserTable() {
        try {
            con = db.getConnection();
            String query = "SELECT user_id, user_name, access_code as password, role FROM user ORDER BY user_id";
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();

            // Setup table model
            tableModel = (DefaultTableModel) userTable.getModel();
            tableModel.setRowCount(0);
            tableModel.setColumnIdentifiers(new String[]{"User ID", "Username", "Password", "Role"});

            while (rs.next()) {
                Object[] row = new Object[4];
                row[0] = rs.getInt("user_id");
                row[1] = rs.getString("user_name");
                row[2] = rs.getString("password"); // access_code
                row[3] = rs.getString("role");
                tableModel.addRow(row);
            }

            con.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading users: " + e.getMessage());
        }
    }

    //TABLE SELECTION LISTENER
    private void setupTableSelectionListener() {
        userTable.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && userTable.getSelectedRow() != -1) {
                int selectedRow = userTable.getSelectedRow();
                selectedUserId = (int) tableModel.getValueAt(selectedRow, 0);
                String username = (String) tableModel.getValueAt(selectedRow, 1);
                String password = (String) tableModel.getValueAt(selectedRow, 2);
                String role = (String) tableModel.getValueAt(selectedRow, 3);

                usernameTxt.setText(username);
                passwordTxt.setText(password);
                roleCombobox.setSelectedItem(role);
            }
        });
    }
    
